const copyTargets = Array.from(document.querySelectorAll("[data-copy]"));
const languageButtons = Array.from(document.querySelectorAll("[data-language-button]"));
const revealTargets = Array.from(document.querySelectorAll("[data-reveal]"));

function setLanguage(language) {
  const selectedLanguage = copyByLanguage[language] ? language : "en";

  // Persist language so returning visitors are not forced to switch every time.
  window.localStorage.setItem("preferred-language", selectedLanguage);
  document.documentElement.lang = selectedLanguage;

  copyTargets.forEach((target) => {
    const key = target.getAttribute("data-copy");
    const value = copyByLanguage[selectedLanguage][key];
    if (value) {
      target.textContent = value;
    }
  });

  languageButtons.forEach((button) => {
    const buttonLanguage = button.getAttribute("data-language-button");
    button.classList.toggle("is-active", buttonLanguage === selectedLanguage);
  });
}

function resolveInitialLanguage() {
  const storedLanguage = window.localStorage.getItem("preferred-language");
  if (storedLanguage && copyByLanguage[storedLanguage]) {
    return storedLanguage;
  }

  return navigator.language.toLowerCase().startsWith("zh") ? "zh" : "en";
}

function setupLanguageSwitch() {
  languageButtons.forEach((button) => {
    button.addEventListener("click", () => {
      setLanguage(button.getAttribute("data-language-button"));
    });
  });
}

function setupRevealAnimations() {
  if (!("IntersectionObserver" in window)) {
    revealTargets.forEach((target) => target.classList.add("is-visible"));
    return;
  }

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.16 }
  );

  revealTargets.forEach((target, index) => {
    target.style.transitionDelay = `${index * 55}ms`;
    observer.observe(target);
  });
}

setupLanguageSwitch();
setLanguage(resolveInitialLanguage());
setupRevealAnimations();
